function renderDOM(data) {
  combination.innerHTML = null;
  data.forEach(function (el) {
    let card = document.createElement("div");

    let img = document.createElement("img");
    img.src = el.img;

    let name = document.createElement("p");
    name.innerText = el.name;

    card.append(img, name);
    combination.append(card);
  });
}


 function renderpagination(pages1) {
   for (let i = 1; i <= pages1; i++) {
     let btn = document.createElement("button");
     btn.textContent = i;
     btn.addEventListener("click", function () {
       currentpage1 = i;

       paginatedTable(product_page_report, currentpage1, perpage1);
     });
     paginated.append(btn);
   }
 }
 renderpagination(pages1);

  function paginatedTable(data, page1, onepage1) {
    let start = onepage1 * (page1 - 1);
    let end = onepage1 * page1;
    let splited = data.slice(start, end);
    renderDOM(splited);
  }

